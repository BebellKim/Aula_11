package Aula_11;

public class Cadeira {
	
	String Cor;
	String Modelo;
	String Tamanho;
	int ID;
	
	public String getCor() {
		return Cor;

	}
	public void setCor(String Cor) {
		this.Cor = Cor;
	}
	public String getModelo() {
		return Modelo;

	}
	public void setModelo(String Modelo) {
		this.Modelo = Modelo;
	}
	public String getTamanho() {
		return Tamanho;

	}
	public void setTamanho(String Tamanho) {
		this.Tamanho = Tamanho;
	}
	public int getID() {
		return ID;

	}
	public void setID(int ID) {
		this.ID = ID;
	}
	public String Subir() {
		return "Subir";
	}
	public String Descer() {
		return "Descer";
	}
	public String Girar() {
		return "Girar";
	}
	public String Enclinar() {
		return "Enclinar";
	}
	
	
	
	


}
