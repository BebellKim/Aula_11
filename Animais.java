package Aula_11;

public class Animais {

	String Raca;
	String Cor;
	String Nome;
	int Idade;
	


	public String getRaca() {
		return Raca;

	}
	public void setRaca(String Raca) {
		this. Raca = Raca;
	}

	public String getCor() {
		return Cor;
	}
	public void setCor(String Cor) {
		this. Cor = Cor;
	}

	public String getNome() {
		return Nome;

	}
	public void setNome(String Nome) {
		this. Nome = Nome;	

	}
	public int getIdade() {
		return Idade;
	}
	public void setIdade(int Idade) {
		this.Idade = Idade;
	}

	public String Pular() {
		return "Pular";
	}
	public String Andar() {
		return "Andar";
	}
	public String Dormir() {
		return "Dormir";
	}
	public String Procriar() {
		return "Procriar";
	}

	public String Comer() {
		return "Comer";
	}
}